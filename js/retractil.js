jQuery(document).ready(function ($) {

  $("#toggle").click(function () {
    $("#wrap").slideToggle("normal");

    if ($("#toggle").text() == "Transporte marítimo ") {
      $("#toggle").html("Transporte marítimo  <span class='fa fa-angle-double-down'></span>")
    }
    else {
      $("#toggle").html("Transporte marítimo <span class='fa fa-angle-double-right'></span>")

    }

  });

});


